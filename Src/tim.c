void blinkD1(bool newState)
{
	if (newState) 
	{
		HAL_TIM_OC_Stop(&htim3, TIM_CHANNEL_4);
		reconfigureToBlink();
		HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_4);
	} 
	else
	{
		HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_4);
		reconfigureToForceOutput();
		HAL_TIM_OC_Start(&htim3, TIM_CHANNEL_4);
	}
}
